export function pickRandomComposeBodyPlaceholder(): string {
  if (COMPOSE_BODY_PLACEHOLDERS.length === 0) return "";
  const i = Math.floor(Math.random() * COMPOSE_BODY_PLACEHOLDERS.length);
  return COMPOSE_BODY_PLACEHOLDERS[i] ?? "";
}
